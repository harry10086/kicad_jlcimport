# C3646938 — back-mounted LED (original issue #89 example)
# ---------------------------------------------------------------------------
class TestC3646938RoundedRect:
    """C3646938: four rounded corners, each a 0.7874-unit-radius arc.

    Original issue #89 example: back-mounted LED, layer-11 npth maps to Edge.Cuts.
    """

    @pytest.fixture(autouse=True)
    def setup(self):
        self.shapes, self.ox, self.oy = _load_footprint("C3646938")
        self.npth_svg = _find_npth_svg(self.shapes)

    def test_npth_shape_has_arc_commands(self):
        """The raw SVG path must contain arc (A) commands."""
        assert self.npth_svg is not None
        assert "A" in self.npth_svg

    def test_old_parser_loses_corners(self):
        """_parse_svg_polygon (the old code path) produces only 4 points — no arcs."""
        old_points = _parse_svg_polygon(self.npth_svg)
        assert len(old_points) == 4

    def test_arc_parser_preserves_corners(self):
        """_parse_svg_path_with_arcs produces many points for the 4 corner arcs."""
        new_points = _parse_svg_path_with_arcs(self.npth_svg)
        assert len(new_points) >= 20

    def test_full_parse_produces_edge_cuts_region(self):
        """Layer-11 npth SOLIDREGION must land on Edge.Cuts with rounded corners."""
        fp = parse_footprint_shapes(self.shapes, self.ox, self.oy)
        edge_cuts = [r for r in fp.regions if r.layer == "Edge.Cuts"]
        assert len(edge_cuts) == 1
        assert edge_cuts[0].region_type == "npth"
        assert len(edge_cuts[0].points) >= 20

    def test_bounding_box_is_square(self):
        """The rounded rectangle should be ~2mm × 2mm (centered)."""
        fp = parse_footprint_shapes(self.shapes, self.ox, self.oy)
        region = next(r for r in fp.regions if r.layer == "Edge.Cuts")
        xs = [p[0] for p in region.points]
        ys = [p[1] for p in region.points]
        width = max(xs) - min(xs)
        height = max(ys) - min(ys)
        assert abs(width - 2.0) < 0.01, f"Expected ~2.0mm width, got {width:.4f}"
        assert abs(height - 2.0) < 0.01, f"Expected ~2.0mm height, got {height:.4f}"

    def test_corners_are_rounded(self):
        """Points along each corner arc must lie on a circle of the expected radius."""
        fp = parse_footprint_shapes(self.shapes, self.ox, self.oy)
        region = next(r for r in fp.regions if r.layer == "Edge.Cuts")
        pts = region.points
        xs = [p[0] for p in pts]
        ys = [p[1] for p in pts]
        half_w = (max(xs) - min(xs)) / 2
        cx = (max(xs) + min(xs)) / 2
        cy = (max(ys) + min(ys)) / 2

        expected_radius = mil_to_mm(0.7874)
        inner = half_w - expected_radius

        corner_pts = [p for p in pts if p[0] > cx + inner * 0.5 and p[1] < cy - inner * 0.5]
        assert len(corner_pts) >= 4, "Expected several arc points in top-right corner"
        for px, py in corner_pts:
            dist = math.sqrt((px - (cx + inner)) ** 2 + (py - (cy - inner)) ** 2)
            assert abs(dist - expected_radius) < 0.01, (
                f"Point ({px:.4f},{py:.4f}) is {dist:.4f}mm from corner centre, expected {expected_radius:.4f}mm"
            )

    def test_kicad_output_not_empty(self):
        """write_footprint should produce valid KiCad output with the region."""
        fp = parse_footprint_shapes(self.shapes, self.ox, self.oy)
        content = write_footprint(fp, "C3646938")
        assert "fp_poly" in content
        assert '"Edge.Cuts"' in content


