# Visual comparison SVG generator (run with pytest -s to see output paths)
# ---------------------------------------------------------------------------
class TestGenerateVisualComparisons:
    """Generate before/after SVGs for visual inspection.

    Run with: pytest tests/test_issue89_rounded_corners.py -k visual -s
    """

    def test_generate_visual_comparisons(self):
        """Produce overlay SVGs: red dashed = old (buggy), green solid = fixed."""
        visual_dir = Path(__file__).parent / "visual"
        visual_dir.mkdir(exist_ok=True)

        for part in ["C3646936", "C3646937", "C3646938"]:
            shapes, ox, oy = _load_footprint(part)
            npth_svg = _find_npth_svg(shapes)
            assert npth_svg is not None

            ox_mm, oy_mm = mil_to_mm(ox), mil_to_mm(oy)
            new_pts = [(x - ox_mm, y - oy_mm) for x, y in _parse_svg_path_with_arcs(npth_svg)]
            old_pts = [(x - ox_mm, y - oy_mm) for x, y in _parse_svg_polygon(npth_svg)]

            svg = _build_comparison_svg(part, new_pts, old_pts)
            out = visual_dir / f"{part}_npth_comparison.svg"
            out.write_text(svg)
            print(f"  Wrote {out}")

        for part in ["C3646936", "C3646937", "C3646938"]:
            shapes, ox, oy = _load_footprint(part)
            fp = parse_footprint_shapes(shapes, ox, oy)
            content = write_footprint(fp, part)
            out = visual_dir / f"{part}.kicad_mod"
            out.write_text(content)
            print(f"  Wrote {out}")


def _build_comparison_svg(part, new_pts, old_pts, width=400, height=400):
    """Build an SVG overlaying old (red dashed) and new (green solid) polygons."""
    all_pts = new_pts + old_pts
    xs = [p[0] for p in all_pts]
    ys = [p[1] for p in all_pts]
    margin = 0.2
    min_x, max_x = min(xs) - margin, max(xs) + margin
    min_y, max_y = min(ys) - margin, max(ys) + margin
    vw = max_x - min_x
    vh = max_y - min_y
    scale = min(width / vw, height / vh) * 0.8

    def tx(x):
        return (x - min_x) * scale + (width - vw * scale) / 2

    def ty(y):
        return (y - min_y) * scale + (height - vh * scale) / 2

    lines = [
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{width}" height="{height}"'
        f' viewBox="0 0 {width} {height}" style="background:#1a1a2e;">',
        f'<text x="{width / 2}" y="25" text-anchor="middle" fill="white"'
        f' font-family="monospace" font-size="14">{part}: NPTH Edge.Cuts board window</text>',
    ]

    if old_pts:
        d = "M " + " L ".join(f"{tx(x):.1f},{ty(y):.1f}" for x, y in old_pts) + " Z"
        lines.append(
            f'<path d="{d}" fill="rgba(255,68,68,0.15)" stroke="#ff4444" stroke-width="2" stroke-dasharray="6,4"/>'
        )

    if new_pts:
        d = "M " + " L ".join(f"{tx(x):.1f},{ty(y):.1f}" for x, y in new_pts) + " Z"
        lines.append(f'<path d="{d}" fill="rgba(68,255,68,0.1)" stroke="#44ff44" stroke-width="2"/>')

    for x, y in new_pts:
        lines.append(f'<circle cx="{tx(x):.1f}" cy="{ty(y):.1f}" r="2" fill="#44ff44" opacity="0.6"/>')
    for x, y in old_pts:
        lines.append(f'<circle cx="{tx(x):.1f}" cy="{ty(y):.1f}" r="4" fill="#ff4444" opacity="0.8"/>')

    ly = height - 40
    lines.append(
        f'<line x1="20" y1="{ly}" x2="50" y2="{ly}" stroke="#ff4444" stroke-width="2" stroke-dasharray="6,4"/>'
    )
    lines.append(
        f'<text x="55" y="{ly + 4}" fill="#ff4444" font-family="monospace"'
        f' font-size="11">Before fix ({len(old_pts)} pts)</text>'
    )
    lines.append(f'<line x1="20" y1="{ly + 18}" x2="50" y2="{ly + 18}" stroke="#44ff44" stroke-width="2"/>')
    lines.append(
        f'<text x="55" y="{ly + 22}" fill="#44ff44" font-family="monospace"'
        f' font-size="11">After fix ({len(new_pts)} pts)</text>'
    )
    lines.append("</svg>")
    return "\n".join(lines)
