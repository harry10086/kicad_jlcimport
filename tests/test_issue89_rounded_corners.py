"""Tests for issue #89: Rounded rectangle corners in EasyEDA footprints.

Uses real component data from the issue report to verify that SOLIDREGION shapes
with SVG arc commands produce correct rounded corner geometry instead of
distorted straight-line polygons.

Layer mapping tested here:
  - Layer 11 npth SOLIDREGION → Edge.Cuts (board window cutout for back-mount LEDs)
  - Layer 12 solid SOLIDREGION → F.Fab (pad-edge markers, polarity indicators)
  - Layer 3 solid SOLIDREGION  → F.SilkS (silkscreen fill indicators)

Visual comparison SVGs are regenerated by test_generate_visual_comparisons()
into tests/visual/ for manual inspection.
"""

import json
import math
from pathlib import Path

import pytest

from kicad_jlcimport.easyeda.parser import (
    _parse_svg_path_with_arcs,
    _parse_svg_polygon,
    mil_to_mm,
    parse_footprint_shapes,
)
from kicad_jlcimport.kicad.footprint_writer import write_footprint

TESTDATA = Path(__file__).parent.parent / "testdata"


def _load_footprint(part_id):
    """Load footprint shapes and origin from testdata JSON."""
    with open(TESTDATA / f"{part_id}_footprint.json") as f:
        data = json.load(f)
    ds = data["dataStr"]
    return ds["shape"], ds["head"]["x"], ds["head"]["y"]


def _find_npth_svg(shapes):
    """Extract the SVG path string from the NPTH SOLIDREGION in a shape list."""
    for s in shapes:
        parts = s.split("~")
        if parts[0] != "SOLIDREGION":
            continue
        svg_path = ""
        rtype = "solid"
        for p in parts[2:]:
            p = p.strip()
            if p.startswith("M") and len(p) > 1 and (p[1].isdigit() or p[1] in " \t-"):
                svg_path = p
            elif p in ("npth", "solid", "cutout"):
                rtype = p
        if rtype == "npth" and svg_path:
            return svg_path
    return None


