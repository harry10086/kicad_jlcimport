# C3646936 — 1206 SMD LED (orange), layer-11 body fill between pads
# ---------------------------------------------------------------------------
class TestC3646936BodyFill:
    """C3646936: XL-3216UOC-FB — layer 11 npth board cutout + rounded corners.

    This is a reverse-mount (bottom-mount) LED.  The layer-11 NPTH SOLIDREGION
    is the board window cutout through which the LED shines, and must land on
    Edge.Cuts with arc-interpolated rounded corners.
    """

    @pytest.fixture(autouse=True)
    def setup(self):
        self.shapes, self.ox, self.oy = _load_footprint("C3646936")
        self.npth_svg = _find_npth_svg(self.shapes)

    def test_has_npth_arc_shape(self):
        """The raw data must contain an NPTH SOLIDREGION with arc commands."""
        assert self.npth_svg is not None
        assert "A" in self.npth_svg

    def test_old_parser_loses_corners(self):
        """_parse_svg_polygon (straight-line only) must drop the arc points."""
        old_pts = _parse_svg_polygon(self.npth_svg)
        assert len(old_pts) == 4, f"Expected 4 straight-line points, got {len(old_pts)}"

    def test_arc_parser_preserves_corners(self):
        """_parse_svg_path_with_arcs must interpolate each arc corner."""
        new_pts = _parse_svg_path_with_arcs(self.npth_svg)
        assert len(new_pts) >= 20, f"Expected ≥20 arc-interpolated points, got {len(new_pts)}"

    def test_board_cutout_on_edge_cuts(self):
        """Layer-11 npth SOLIDREGION must land on Edge.Cuts (board window cutout)."""
        fp = parse_footprint_shapes(self.shapes, self.ox, self.oy)
        edge_cuts = [r for r in fp.regions if r.layer == "Edge.Cuts"]
        assert len(edge_cuts) == 1, "Expected exactly one Edge.Cuts region for the board cutout"
        assert edge_cuts[0].region_type == "npth"

    def test_cutout_inside_pad_extents(self):
        """The board window sits between the pads (x-extent < pad x-extent)."""
        fp = parse_footprint_shapes(self.shapes, self.ox, self.oy)
        region = next(r for r in fp.regions if r.layer == "Edge.Cuts")
        xs = [pt[0] for pt in region.points]
        body_half_x = (max(xs) - min(xs)) / 2
        pad_half_x = max(abs(p.x) + p.width / 2 for p in fp.pads)
        assert body_half_x < pad_half_x, (
            f"Cutout x half-extent ({body_half_x:.3f}mm) must be less than pad x half-extent ({pad_half_x:.3f}mm)"
        )

    def test_cutout_bounding_box(self):
        """Board window should be approximately 2.2 × 1.9 mm."""
        fp = parse_footprint_shapes(self.shapes, self.ox, self.oy)
        region = next(r for r in fp.regions if r.layer == "Edge.Cuts")
        xs = [pt[0] for pt in region.points]
        ys = [pt[1] for pt in region.points]
        width = max(xs) - min(xs)
        height = max(ys) - min(ys)
        assert abs(width - 2.2) < 0.05, f"Expected ~2.2mm width, got {width:.4f}mm"
        assert abs(height - 1.9) < 0.05, f"Expected ~1.9mm height, got {height:.4f}mm"

    def test_corners_are_rounded(self):
        """Arc-interpolated points must lie on the corner arc, not a straight line."""
        fp = parse_footprint_shapes(self.shapes, self.ox, self.oy)
        region = next(r for r in fp.regions if r.layer == "Edge.Cuts")
        pts = region.points
        xs = [p[0] for p in pts]
        ys = [p[1] for p in pts]
        cx = (max(xs) + min(xs)) / 2
        cy = (max(ys) + min(ys)) / 2
        half_w = (max(xs) - min(xs)) / 2
        half_h = (max(ys) - min(ys)) / 2

        # Arc centre for top-right corner is inset from the outer corner by r
        # in both axes independently (handles non-square shapes correctly).
        expected_radius = mil_to_mm(0.7874)
        corner_cx = cx + (half_w - expected_radius)
        corner_cy = cy - (half_h - expected_radius)

        corner_pts = [
            p
            for p in pts
            if p[0] > cx + (half_w - expected_radius) * 0.5 and p[1] < cy - (half_h - expected_radius) * 0.5
        ]
        assert len(corner_pts) >= 4, "Expected several arc-interpolated points in top-right corner"
        for px, py in corner_pts:
            dist = math.sqrt((px - corner_cx) ** 2 + (py - corner_cy) ** 2)
            assert abs(dist - expected_radius) < 0.01, (
                f"Point ({px:.4f},{py:.4f}) is {dist:.4f}mm from corner centre, expected {expected_radius:.4f}mm"
            )

    def test_has_two_pads(self):
        fp = parse_footprint_shapes(self.shapes, self.ox, self.oy)
        assert len(fp.pads) == 2

    def test_fab_polarity_markers(self):
        """Layer-12 solid regions (pad-edge markers) must appear on F.Fab."""
        fp = parse_footprint_shapes(self.shapes, self.ox, self.oy)
        fab_solid = [r for r in fp.regions if r.layer == "F.Fab" and r.region_type == "solid"]
        assert len(fab_solid) >= 2, "Expected ≥2 F.Fab solid regions for polarity markers"

    def test_kicad_output_contains_edge_cuts_poly(self):
        fp = parse_footprint_shapes(self.shapes, self.ox, self.oy)
        content = write_footprint(fp, "C3646936")
        assert "fp_poly" in content
        assert '"Edge.Cuts"' in content


